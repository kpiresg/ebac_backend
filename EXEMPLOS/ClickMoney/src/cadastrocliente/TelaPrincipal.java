    private void autoMoneyUpgradeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_autoMoneyUpgradeMouseClicked
        
        if(autoClickUpgradeValue > cookie) {
            JOptionPane.showMessageDialog(null, "Você não tem dinheiro suficiente para realizar esse upgrade", "upgrade click", JOptionPane.INFORMATION_MESSAGE);
        } else if (autoClickUpgradeValue <= cookie) {
            cookie = cookie - autoClickUpgradeValue;
            
            clickLabel.setText("Money: " + this.cookie);
            autoClickUpgradeValue *= 1.5;
            valueOfAutoMoneyUpgrade.setText(autoClickUpgradeValue + "$");
            
            int cont = 0;
            cont++;
            
            timerMoney(cont);
        }
    }//GEN-LAST:event_autoMoneyUpgradeMouseClicked

    private void timerMoney(int cont) {
    
        Timer timer = new Timer();
        
        Long delay = (1000 * 2l);
        
        TimerTask addMoney = new TimerTask() {
            @Override
            public void run() {
                cookie = cookie + cont;
                clickLabel.setText("Money: " + cookie);
            }
        };
    
        timer.scheduleAtFixedRate(addMoney, 0, delay);
        
    }
    
    
    

    private void upgradeClickButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_upgradeClickButtonMouseClicked
        if(this.clickUpgradeValue > this.cookie) {
        JOptionPane.showMessageDialog(null, "Você não tem dinheiro suficiente para realizar esse upgrade", "upgrade click", JOptionPane.INFORMATION_MESSAGE);
        } else if(this.clickUpgradeValue <= this.cookie) {
        this.cookie = this.cookie - this.clickUpgradeValue;
        clickLabel.setText("Money: " + this.cookie);
        
        this.clickCookie++;
        this.clickUpgradeValue *= 1.5;
        valueOfUpgradeClick.setText(this.clickUpgradeValue + "$");
        }
    }//GEN-LAST:event_upgradeClickButtonMouseClicked

